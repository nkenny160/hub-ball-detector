#include <cstring>
#include <fcntl.h>
#include <linux/i2c-dev.h>
#include <sys/ioctl.h>
#include <unistd.h>

/**
 * SSD1306 128x64 OLED driver over I2C (no external library required).
 *
 * Default I2C address: 0x3C (most common); use 0x3D if SA0 pin is high.
 * Wiring on RPi 3B: SDA → GPIO2 (pin 3), SCL → GPIO3 (pin 5), 3.3 V, GND.
 *
 * Text rendering: 8 rows × 21 columns using a built-in 6×8 font.
 */
class OLED_I2C {
public:
    static constexpr uint8_t DEFAULT_ADDR = 0x3C;
    static constexpr int WIDTH  = 128;
    static constexpr int HEIGHT = 64;
    static constexpr int PAGES  = HEIGHT / 8;   // 8 pages

    explicit OLED_I2C(uint8_t addr = DEFAULT_ADDR, const char* dev = "/dev/i2c-1")
        : _addr(addr)
    {
        _fd = open(dev, O_RDWR);
        if (_fd < 0) return;
        if (ioctl(_fd, I2C_SLAVE, _addr) < 0) { close(_fd); _fd = -1; return; }
        init();
        clear();
        flush();
    }

    ~OLED_I2C() { if (_fd >= 0) close(_fd); }

    bool ok() const { return _fd >= 0; }

    /** Fill framebuffer with zeros. Call flush() to push to screen. */
    void clear() { memset(_buf, 0, sizeof(_buf)); }

    /** Push framebuffer to display. */
    void flush()
    {
        // Set page/column range for the whole screen
        send_cmd(0x21); send_cmd(0); send_cmd(127); // column 0-127
        send_cmd(0x22); send_cmd(0); send_cmd(7);   // page 0-7

        // Send framebuffer in chunks (I2C write limit)
        uint8_t tmp[17];
        tmp[0] = 0x40; // data mode
        for (int i = 0; i < WIDTH * PAGES; i += 16) {
            memcpy(tmp + 1, _buf + i, 16);
            write(_fd, tmp, 17);
        }
    }

    /**
     * Draw text at character grid position (col 0..20, row 0..7).
     * Does NOT auto-flush — call flush() after all drawing is done.
     */
    void drawText(int col, int row, const char* str)
    {
        while (*str && col < 21) {
            draw_char(col++, row, static_cast<uint8_t>(*str++));
        }
    }

    /** Convenience: clear, draw two lines, flush. */
    void showCount(int count)
    {
        char line1[22];
        char line2[22];
        std::snprintf(line1, sizeof(line1), "   Ball Counter");
        std::snprintf(line2, sizeof(line2), "   Count: %d", count);

        clear();
        drawText(0, 2, line1);
        drawText(0, 4, line2);
        flush();
    }

private:
    // ------- 6×8 font (ASCII 0x20–0x7E) --------------------------------
    // Each character is 5 columns of pixel data + 1 blank column spacing.
    // Rows are bits 0 (top) through 7 (bottom).
    static const uint8_t FONT[95][5];

    int     _fd = -1;
    uint8_t _addr;
    uint8_t _buf[WIDTH * PAGES]{};  // framebuffer

    void send_cmd(uint8_t c)
    {
        uint8_t buf[2] = {0x00, c};
        write(_fd, buf, 2);
    }

    void draw_char(int col, int row, uint8_t ch)
    {
        if (ch < 0x20 || ch > 0x7E) ch = 0x20;
        const uint8_t* glyph = FONT[ch - 0x20];
        int x = col * 6;
        if (x + 5 > WIDTH) return;
        for (int i = 0; i < 5; ++i)
            _buf[row * WIDTH + x + i] = glyph[i];
        _buf[row * WIDTH + x + 5] = 0x00; // column gap
    }

    void init()
    {
        static const uint8_t cmds[] = {
            0xAE,       // display off
            0xD5, 0x80, // clock div / osc freq
            0xA8, 0x3F, // multiplex ratio (64 rows - 1)
            0xD3, 0x00, // display offset
            0x40,       // start line 0
            0x8D, 0x14, // charge pump on
            0x20, 0x00, // horizontal addressing mode
            0xA1,       // segment re-map (mirror horizontally)
            0xC8,       // COM scan direction (mirror vertically)
            0xDA, 0x12, // COM pins config
            0x81, 0xCF, // contrast
            0xD9, 0xF1, // pre-charge period
            0xDB, 0x40, // VCOMH level
            0xA4,       // display from RAM
            0xA6,       // normal (non-inverted) display
            0xAF,       // display on
        };
        for (uint8_t c : cmds) send_cmd(c);
    }
};

